package actualCombat

import "fmt"

type Light struct {
	isRunning bool
}

func (t *Light) On() {
	t.isRunning = true
	fmt.Println("打开灯...")
}

func (t *Light) Off() {
	t.isRunning = false
	fmt.Println("关闭灯...")
}
